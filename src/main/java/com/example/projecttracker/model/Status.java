package com.example.projecttracker.model;

/**
 * Enum for the status of a task or issue.
 *
 * @author Alyssa Heimlicher
 * @version 1.0
 * @since 2022-05-20
 */
public enum Status {
    /**
     * Status of a task or issue that is still open.
     *
     * @since 1.0
     */
    TODO,
    /**
     * Status of a task or issue that is in progress.
     *
     * @since 1.0
     */
    IN_PROGRESS,
    /**
     * Status of a task or issue that is complete.
     *
     * @since 1.0
     */
    COMPLETED,
}
